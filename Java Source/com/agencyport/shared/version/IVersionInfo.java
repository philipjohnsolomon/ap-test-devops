package com.agencyport.shared.version;


/**
 * The VersionInfo interface contains constants for versioning.
 */
public interface IVersionInfo {
	/**
	 * The <code>DEFAULT_MAJOR_MAJOR_VERSION</code> is the major major version number.
	 */
	int DEFAULT_MAJOR_MAJOR_VERSION = 5;
	
	/**
	 * The <code>DEFAULT_MAJOR_MINOR_VERSION</code> is the major minor version number.
	 */
	int DEFAULT_MAJOR_MINOR_VERSION = 0;
	/**
	 * The <code>DEFAULT_MINOR_MAJOR_VERSION</code> is the minor major version number.
	 */
	int DEFAULT_MINOR_MAJOR_VERSION = 0;
	/**
	 * The <code>DEFAULT_MINOR_MINOR_VERSION</code> is the minor minor version number.
	 */
	int DEFAULT_MINOR_MINOR_VERSION = 0;
}
